# Customer churn prediction

# Import required libraries
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder , StandardScaler
from sklearn.linear_model import LogisticRegression
from sklearn.ensemble import RandomForestClassifier
from sklearn.metrics import accuracy_score, confusion_matrix, classification_report, roc_auc_score

# Load Dataset

# Sample telecom-style dataset
data = data = {
    "tenure": [1, 3, 12, 24, 5, 8, 30, 18, 2, 15],
    "monthly_charges": [70, 85, 60, 55, 90, 75, 50, 65, 95, 58],
    "total_charges": [70, 255, 720, 1320, 450, 600, 1500, 1170, 190, 870],
    "contract": ["Month-to-month", "Month-to-month", "One year", "Two year",
                 "Month-to-month", "One year", "Two year", "One year",
                 "Month-to-month", "Two year"],
    "churn": ["Yes", "Yes", "No", "No", "Yes", "No", "No", "No", "Yes", "No"]
}

df = pd.DataFrame(data)

#  Encode Categorical Data

le = LabelEncoder()
df["contract"] = le.fit_transform(df["contract"])
df["churn"] = le.fit_transform(df["churn"])



# Feature and target split
X = df.drop("churn", axis=1)
y = df["churn"]

# Train test split

X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.25, random_state=42
)

scaler = StandardScaler()
X_train = scaler.fit_transform(X_train)
X_test = scaler.transform(X_test)

#  Logistic Regression Model

log_model = LogisticRegression()
log_model.fit(X_train, y_train)

log_pred = log_model.predict(X_test)

print("Logistic Regression Accuracy:", accuracy_score(y_test, log_pred))

# Random Forest Model
rf_model = RandomForestClassifier(n_estimators=100, random_state=42)
rf_model.fit(X_train, y_train)

rf_pred = rf_model.predict(X_test)
print("Random Forest Accuracy:", accuracy_score(y_test, rf_pred))

# Evaluation
print("\n Classification Report (Random Forest):")
print(classification_report(y_test, rf_pred))

# Confusion matrix
cm = confusion_matrix(y_test, rf_pred)
sns.heatmap(cm, annot=True, fmt="d", cmap="Blues")
plt.title("Confusion Matrix")
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.show()

# Feature Importance

importance = rf_model.feature_importances_
features = df.drop("churn", axis =1).columns

plt.barh(features, importance)
plt.xlabel("Importance_score")

plt.title("Feature Importance - churn prediction")
plt.show()

